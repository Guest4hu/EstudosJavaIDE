package aulas.licoesDeAula.aula11;

public class VariaveisInteiras {
    public static void main(String[] args) {
        // Variaveis inteiras
        byte idade01 = 21; // -128 a 127
        short idade02 = 21; // -32.768 a 32.767
        int idade03 = 21; // -2.147.483.648 a 2.147.483.647
        long idade04 = 21L; // -9.223.372.036.854.775.808 a 9.223.372.036.854.775.807

        System.out.println("Valor variavel de tipo byte: " + idade01);
        System.out.println("Valor variavel de tipo short: " + idade02);
        System.out.println("Valor variavel de tipo int: " + idade03);
        System.out.println("Valor variavel de tipo long: " + idade04);
    }
}

package aulas.licoesDeAula.aula13;

public class OperadoresRelacionais {
    public static void main(String[] args) {
        int numero01 = 1;
        int numero02 = 2;

        System.out.println("Comparação entre os numeros: == " + (numero01 == numero02));
        System.out.println("Comparação entre os numeros: != " + (numero01 != numero02));
        System.out.println("Comparação entre os numeros: > " + (numero01 > numero02));
        System.out.println("Comparação entre os numeros: < " + (numero01 < numero02));

    }
}

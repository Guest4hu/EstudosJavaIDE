package aulas.licoesDeAula.aula13;

public class OperadoresAritmeticos {
    public static void main(String[] args) {
        int resultado = 1 + 2;
        System.out.println(resultado);

        resultado = resultado - 2;
        System.out.println(resultado);

        resultado = resultado * 2;
        System.out.println(resultado);

        resultado = resultado / 2;
        System.out.println(resultado);

        resultado = resultado + 2;
        System.out.println(resultado);


        resultado = resultado % 2;
        System.out.println(resultado);


        String primeiraParte = "Isto e uma";
        String segundaParte = "String concatenada";
        String concatenada = primeiraParte + " " + segundaParte;
        System.out.println(concatenada);

    }

}
